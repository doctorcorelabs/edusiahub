// Function to merge AI translations with existing translations
export const mergeAITranslations = (existingTranslations, language) => {
  const aiTranslation = aiTranslations[language];
  if (!aiTranslation) {
    console.warn(`AI translations not found for language: ${language}`);
    return existingTranslations;
  }

  return {
    ...existingTranslations,
    ...aiTranslation
  };
}; 